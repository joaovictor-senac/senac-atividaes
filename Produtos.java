public class Produtos {


    private String produto;

    private String descricao;

    private String dataDeVecimento;

    private int valor;

    private int valorPromocao;





    public String getProduto() {
        return produto;
    }

    public void setProduto(String produto) {
        this.produto = produto;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDataDeVecimento() {
        return dataDeVecimento;
    }

    public void setDataDeVecimento(String dataDeVecimento) {
        this.dataDeVecimento = dataDeVecimento;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getValorPromocao() {
        return valorPromocao;
    }

    public void setValorPromocao(int valorPromocao) {
        this.valorPromocao = valorPromocao;
    }


}
